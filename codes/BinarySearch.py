def binary_search(arr, target):
    """
    Выполняет бинарный поиск элемента target в отсортированном массиве arr.
    Возвращает индекс элемента, если он найден, иначе -1.
    """

    # Инициализация границ поиска: левая и правая
    left, right = 0, len(arr) - 1

    # Пока границы не пересеклись
    while left <= right:
        # Находим середину сегмента
        mid = (left + right) // 2
        
        # Если элемент найден, возвращаем индекс
        if arr[mid] == target:
            return mid
        
        # Если элемент больше среднего, ищем в правой половине
        elif arr[mid] < target:
            left = mid + 1
        
        # Иначе в левой половине массива
        else:
            right = mid - 1
    
    # Элемент не найден, возвращаем -1
    return -1

# Пример использования функции
arr = [1, 3, 5, 7, 9, 11]
target = 7
index = binary_search(arr, target)
print(f'Индекс элемента {target}: {index}')
